// popup
document.getElementById('profile').addEventListener('click', 
function() {
   document.querySelector('.bg-trans').style.display = "flex"; 
});
// popup close
document.getElementById('close').addEventListener('click', 
function() {
   document.querySelector('.bg-trans').style.display = "none";
})
// power
document.getElementById('power1').addEventListener("change", 
function() {
   if (document.getElementById('power1').checked) {
      document.querySelector('.text1').style.background = "#d7fada"; 
   }else {
      document.querySelector('.text1').style.background = "#fad7d7"; 
   }
});
